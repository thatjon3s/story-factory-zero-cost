  slug text primary key,
  display_name text not null,
  adapter_key text not null,
  enabled boolean not null default false,
  incremental_cost_usd numeric(10,4) not null default 0
    check (incremental_cost_usd >= 0),
  commercial_use_allowed boolean not null default false,
  automation_allowed boolean not null default false,
  watermark_free boolean not null default false,
  supports_dialogue boolean not null default false,
  supports_reference_images boolean not null default false,
  supported_aspect_ratios text[] not null default '{}',
  max_resolution text,
  daily_credit_allowance integer check (daily_credit_allowance is null or daily_credit_allowance >= 0),
  remaining_credits integer check (remaining_credits is null or remaining_credits >= 0),
  credit_cost_per_scene integer not null default 1 check (credit_cost_per_scene > 0),
  next_credit_reset_at timestamptz,
  priority integer not null default 0,
  quality_score numeric(5,2) not null default 0 check (quality_score between 0 and 100),
  success_rate numeric(5,4) not null default 0 check (success_rate between 0 and 1),
  terms_url text not null,
  terms_checked_at timestamptz not null,
  terms_fingerprint text not null,
  secret_names text[] not null default '{}',
  settings jsonb not null default '{}'::jsonb,
  blocked_reason text,
  last_health_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (
    not enabled
    or (
      incremental_cost_usd = 0
      and commercial_use_allowed
      and automation_allowed
      and watermark_free
      and supports_dialogue
      and supports_reference_images
      and '16:9' = any(supported_aspect_ratios)
      and blocked_reason is null
    )
  )
);

