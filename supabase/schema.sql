create table if not exists public.canon_entries (
  id uuid primary key default gen_random_uuid(),
  universe_slug text not null,
  series_slug text not null,
  scope text not null check (scope in ('universe', 'series')),
  canonical_key text not null,
  summary text not null check (length(trim(summary)) > 0),
  importance integer not null default 50 check (importance between 1 and 100),
  status text not null default 'canonical' check (status in ('canonical', 'superseded', 'proposed')),
  source_episode_revision text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (universe_slug, series_slug, canonical_key),
  check (
    (scope = 'universe' and series_slug = '__universe__')
    or (scope = 'series' and series_slug <> '__universe__')
  )
);

create table if not exists public.episodes (
  id bigint generated by default as identity primary key,
  owner_id text not null default 'story-factory',
  universe_name text not null,
