create or replace function public.reset_due_studio_credits()
returns integer
language plpgsql
security invoker
set search_path = ''
as $$
declare
  changed integer;
begin
  update public.studio_providers
     set remaining_credits = daily_credit_allowance,
         next_credit_reset_at = next_credit_reset_at + interval '1 day',
         updated_at = now()
   where daily_credit_allowance is not null
     and next_credit_reset_at is not null
     and next_credit_reset_at <= now();
  get diagnostics changed = row_count;
  return changed;
end;
$$;

create or replace function public.claim_scene_job(
  worker_name text,
  target_episode_revision text,
  lease_seconds integer default 7200
)
returns jsonb
language plpgsql
security invoker
set search_path = ''
as $$
declare
  selected_job public.scene_jobs%rowtype;
  selected_provider public.studio_providers%rowtype;
begin
  if worker_name is null or length(trim(worker_name)) < 3 then
    raise exception 'worker_name is required';
  end if;
  if target_episode_revision is null or length(trim(target_episode_revision)) <> 16 then
    raise exception 'target_episode_revision is required';
  end if;
  if lease_seconds < 60 or lease_seconds > 21600 then
    raise exception 'lease_seconds must be between 60 and 21600';
  end if;

  perform public.reset_due_studio_credits();

  select j.*
    into selected_job
    from public.scene_jobs j
   where (
       j.status in ('queued', 'retry')
       or (j.status in ('leased', 'rendering') and j.lease_until < now())
     )
     and j.episode_revision = target_episode_revision
     and j.not_before <= now()
     and j.attempts < j.max_attempts
   order by j.priority desc, j.created_at
   for update skip locked
   limit 1;

  if selected_job.id is null then
    return null;
  end if;

  select p.*
    into selected_provider
    from public.studio_providers p
   where p.enabled
     and p.incremental_cost_usd = 0
     and p.commercial_use_allowed
     and p.automation_allowed
     and p.watermark_free
     and p.supports_dialogue
     and p.supports_reference_images
     and '16:9' = any(p.supported_aspect_ratios)
     and p.blocked_reason is null
     and p.terms_checked_at >= now() - interval '30 days'
     and p.terms_fingerprint not like 'pending%'
     and (
       p.remaining_credits is null
       or p.remaining_credits >= p.credit_cost_per_scene
     )
   order by p.priority desc, p.quality_score desc, p.success_rate desc
   for update skip locked
   limit 1;

  if selected_provider.slug is null then
    return null;
  end if;

  update public.scene_jobs
     set status = 'leased',
         provider_slug = selected_provider.slug,
         attempts = attempts + 1,
         leased_by = worker_name,
         lease_until = now() + make_interval(secs => lease_seconds),
         updated_at = now()
   where id = selected_job.id
   returning * into selected_job;

  if selected_provider.remaining_credits is not null then
    update public.studio_providers
       set remaining_credits = remaining_credits - credit_cost_per_scene,
           updated_at = now()
     where slug = selected_provider.slug;
  end if;

  return jsonb_build_object(
    'job', to_jsonb(selected_job),
    'provider', to_jsonb(selected_provider)
      - 'settings'
      - 'secret_names'
  );
end;
$$;

